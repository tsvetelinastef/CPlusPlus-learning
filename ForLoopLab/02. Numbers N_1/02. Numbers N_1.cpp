#include <iostream>
using namespace std;

// Напишете програма, която чете цяло положително число n, въведено от потребителя и печата числата от n до 1 в обратен ред.
// Въведеното число n, винаги ще бъде по - голямо от 1.


int main()
{
    int n;
    cin >> n;

    for (int i = n; i > 0; i--)
    {
        cout << i << endl;
    }
    
    return 0;
}

